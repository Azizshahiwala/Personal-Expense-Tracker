def createRoomCode(size=10,chars=string.ascii_uppercase + string.digits):
#This function is used to create an unique roomcode.
#Choice function select random element from non-spaced seq. and range generates 6 items using string passed.
#string.ascii_uppercase generates all letters A-Z
#string.digits generates all numbers 0-9
#chars combine both of them and creates a long string.
#random.SystemRandom() uses OS builtin cryptographic source.
# . choice() picks one character from the string passes.
# for loop iterates for N times. hence, creating unique code.
    roomcode = ''.join(random.SystemRandom().choice(chars) for _ in range(size))

    return roomcode

@router.post("/create")
def createRoom(room: createRoomSchema, db: Session = Depends(get_db), current_user: dict = Depends(get_current_user)):
    
    # 1. Validate the user
    if room.created_by_uuid != current_user['user_id']:
        raise HTTPException(status_code=status.HTTP_403_FORBIDDEN, detail="Invalid operation to create a room.")
    
    # 2. Generate a UNIQUE room code
    while True:
        ROOMCODE = createRoomCode()
        existsroomcode = db.query(Group).filter(Group.invitecode == ROOMCODE).first()
        if not existsroomcode:
            break 
            
    # 3. Create the Group
    entry = Group(
        group_name=room.roomname,
        created_by_id=room.created_by_uuid,
        invitecode=ROOMCODE
    )
    db.add(entry)
    db.commit()
    db.refresh(entry)

    # 4. Add the creator as the first GroupMember
    member = GroupMember(
        group_id=ROOMCODE,              
        user_id=entry.created_by_id,
        is_admin="admin"                
    )
    db.add(member)
    db.commit()
    
    record = db.query(GroupMember).filter(GroupMember.user_id == room.created_by_uuid).first()
    
    if not record:
        raise HTTPException(status_code=500, detail="Failed to retrieve room data after creation.")
        
    # Construct the dictionary directly
    room_data = {
        "group_id": record.group_id,
        "room_code": entry.invitecode,
        "role": record.is_admin,
        "Groupname":room.roomname,
        "RoomCodeVisibility":entry.can_see_invite_code
    }
    #Can see invite code attribute is global constriant
    return room_data
    
@router.post("/join")
def joinRoom(room: joinRoomSchema, db: Session = Depends(get_db), current_user: dict = Depends(get_current_user)):
    try:
        # 1. Validate ownership
        if room.joining_by_uuid != current_user['user_id']:
            raise HTTPException(status_code=status.HTTP_403_FORBIDDEN, detail="There was an error joining a room.")

        # 2. Find room by invite code
        record = db.query(Group).filter(Group.invitecode == room.joincode).first()

        if not record:
            raise HTTPException(status_code=404, detail="Room not found. Check the invite code.")

        # 3. Check not already a member
        already_member = db.query(GroupMember).filter(
            GroupMember.group_id == record.invitecode,
            GroupMember.user_id == room.joining_by_uuid
        ).first()

        if already_member:
            raise HTTPException(status_code=400, detail="You are already a member of this room.")

        # 4. Add as member
        member = GroupMember(
            group_id=record.invitecode,
            user_id=room.joining_by_uuid,
            is_admin="member"
        )
        db.add(member)
        db.commit()
        db.refresh(member)

        #Get username:
        
        username = db.query(Credentials).filter(Credentials.unique_user_id == room.joining_by_uuid).first()

        notif = None

        if username and username:
            print("Username found while leaving:",username)
            #Add a notification:
            notif = ChatMessages(group_id=member.group_id,
                             sender_id=room.joining_by_uuid,
                             message=f"{username.first_name} {username.last_name} has joined the room. Welcome!",
                             timestamp=datetime.now(),
                             msgtype="notification")
            db.add(notif)
            db.commit()
            db.refresh(notif)

        return {
            "group_id": member.group_id,
            "room_code": record.invitecode,
            "role": member.is_admin,
            "CanExportHistory":member.can_export_history,
            "CanChat":member.can_chat,
            "CanUpdateCalendar":member.can_update_calendar,
        }

    except HTTPException as e:
        db.rollback()
        print(e)
        raise
    except Exception as e:
        db.rollback()
        print(f"Join room error: {e}")
        raise HTTPException(status_code=500, detail="Internal server error.")

