@router.post("/dissolve")
def dissolveRoom(room: dissolveRoomSchema, db: Session = Depends(get_db), current_user: dict = Depends(get_current_user)):
    try:
        # 1. Validate the user making the request
        if room.target_uuid != current_user['user_id']:
            raise HTTPException(status_code=status.HTTP_403_FORBIDDEN, detail="Unauthorized operation.")
        
        # 2. Verify the user is an admin of this specific group
        admin_record = db.query(GroupMember).filter(
            GroupMember.user_id == room.target_uuid, 
            GroupMember.group_id == room.groupcode,
            GroupMember.is_admin == "admin"
        ).first()
        
        if not admin_record:
            raise HTTPException(status_code=status.HTTP_403_FORBIDDEN, detail="Only admins can dissolve the room.")
        

        # 3. Clean up all members from the group first to prevent Foreign Key constraint blocks
        db.query(GroupMember).filter(GroupMember.group_id == room.groupcode).delete()
        
        # 4. Delete the group itself
        db.query(Group).filter(Group.invitecode == room.groupcode).delete()
        
        db.commit()
        
        return {"message": "Room dissolved successfully."}
    
    except HTTPException:
        db.rollback()
        raise
    except Exception as e:
        print("error:", e)
        db.rollback()
        raise HTTPException(status_code=status.HTTP_500_INTERNAL_SERVER_ERROR, detail="Failed to dissolve room")
    
